package MathFunctions;

public class MathTest {
	
	public static void main(String[]args) {
		
		System.out.println(Math.random()); //0.1366313314827139
		
		System.out.println(Math.random()*10);//3.248012584290122
		
		System.out.println(Math.random()*100);//11.02662011889699
		
	    //TypeCasting to int
		System.out.println((int)(Math.random()*100));//46
		
		System.out.println((int)(Math.random()*5));// Generates Below 5

}
	
}